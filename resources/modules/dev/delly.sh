#!/bin/bash
# Exit immediately on error
set -eu -o pipefail

################################################################################
# This is a module install script template which should be copied and used for
# consistency between module paths, permissions, etc.
# Only lines marked as "## TO BE ADDED/MODIFIED" should be, indeed, modified.
# Also, once modified, delete this commented-out header and the ## comments
################################################################################

SOFTWARE="delly"
VERSION="0.7.8"
ARCHIVE="$SOFTWARE"_v"$VERSION"_linux_x86_64bit
ARCHIVE_URL="https://github.com/dellytools/$SOFTWARE/releases/download/v$VERSION/$ARCHIVE"
SOFTWARE_DIR="$SOFTWARE-v$VERSION"


# Specific commands to extractÂ and build the software
# $INSTALL_DIR and $INSTALL_DOWNLOAD have been set automatically
# $ARCHIVE has been downloaded in $INSTALL_DOWNLOAD
build() {
  cd $INSTALL_DOWNLOAD
   

	chmod a+x  "$SOFTWARE"_v"$VERSION"_linux_x86_64bit
	mkdir $SOFTWARE_DIR
  cp "$SOFTWARE"_v"$VERSION"_linux_x86_64bit $SOFTWARE_DIR/delly
	

  # Install software
  cd $INSTALL_DOWNLOAD  ## TO BE ADDED AND MODIFIED IF NECESSARY
  mv -i $SOFTWARE_DIR $INSTALL_DIR/  ## TO BE ADDED AND MODIFIED IF NECESSARY
}

module_file() {
echo "\
#%Module1.0
proc ModulesHelp { } {
  puts stderr \"\tMUGQIC - $SOFTWARE \"
}
module-whatis \"$SOFTWARE\"

set             root                $INSTALL_DIR/$SOFTWARE_DIR
prepend-path    PATH                \$root ;  
"
}

# Call generic module install script once all variables and functions have been set
MODULE_INSTALL_SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
source $MODULE_INSTALL_SCRIPT_DIR/install_module.sh $@
